//Demo on CompoundAssignment operators
class Lab60{
public static void main(String args[]){
	  int a = 10;
	   a += 30;  // a = a+30;
	   System.out.println(a);
	   
	   a -= 10;  //a = a-10
	   System.out.println(a);
	   
	   a *= 10;  //a = a*10
	   System.out.println(a);
	   
	   a /= 10;  //a = a/10
	   System.out.println(a);
	   
	    a %= 10;  //a = a%10
	   System.out.println(a);
}
}