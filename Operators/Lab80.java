//Demo on Bitwise Operators
class Lab80{
public static void main(String args[]){
	int i1 = 1;
	int i2 = 2;
	System.out.println(i1<<1);
	System.out.println(i2<<2);
	
	System.out.println(i2>>1);
	
	System.out.println(i1&i2);
	System.out.println(i1|i2);
	
	System.out.println(i1^i2);
	System.out.println(~i2);
}
}

